"""### Task 6: Diagnostic Importance Analysis

To identify which words influence the prediction of a medical specialty, we can examine the weights of the output layer in our trained baseline model. For each specialty, words with higher positive weights contribute more to the model's decision to classify a report into that specialty.
"""

import pandas as pd
import numpy as np

# Get weights from each Dense layer
# model.layers[0] is the first Dense layer (input_dim=5000, output_dim=256)
w1 = model.layers[0].get_weights()[0] # Shape (5000, 256)

# model.layers[2] is the second Dense layer (input_dim=256, output_dim=128)
w2 = model.layers[2].get_weights()[0] # Shape (256, 128)

# model.layers[4] is the third/output Dense layer (input_dim=128, output_dim=40)
w3 = model.layers[4].get_weights()[0] # Shape (128, 40)

# Calculate effective weights from input features to output classes
# This is a linear approximation and ignores non-linear activations and dropout,
# but provides a simple way to interpret feature importance across layers.
effective_weights = np.dot(np.dot(w1, w2), w3) # Resulting shape (5000, 40)

# Get the feature names (words) from the TF-IDF vectorizer
feature_names = tfidf_vectorizer.get_feature_names_out()

# Create a DataFrame to store word importance for each class
word_importance_df = pd.DataFrame(effective_weights, index=feature_names, columns=label_encoder.classes_)

