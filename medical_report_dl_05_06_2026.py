"""### Task 4: Self-Attention Model

To build a self-attention model as requested, we'll follow these steps:
1.  **Text Tokenization and Padding**: Prepare the `cleaned_text` by converting it into sequences of integers and padding them to a uniform length suitable for an embedding layer.
2.  **Model Definition**: Construct a Keras model using `tf.keras.layers.Embedding`, `tf.keras.layers.MultiHeadAttention`, and `tf.keras.layers.Dense` for the output.
3.  **Model Training**: Train the self-attention model on the preprocessed data.
4.  **Model Evaluation**: Evaluate the model's performance on the test set.

#### 1. Text Tokenization and Padding for Embedding Layer
"""

from tensorflow.keras.preprocessing.text import Tokenizer
from tensorflow.keras.preprocessing.sequence import pad_sequences

# Define vocabulary size and maximum sequence length
VOCAB_SIZE = 10000  # Limiting vocabulary to top 10,000 words
MAX_LEN = 200       # Maximum length of text sequences after padding

# Initialize and fit tokenizer on training data
tokenizer = Tokenizer(num_words=VOCAB_SIZE, oov_token="<unk>")
tokenizer.fit_on_texts(X_train)

# Convert text to sequences of integers
X_train_sequences = tokenizer.texts_to_sequences(X_train)
X_test_sequences = tokenizer.texts_to_sequences(X_test)

# Pad sequences to ensure uniform length
X_train_padded = pad_sequences(X_train_sequences, maxlen=MAX_LEN, padding='post', truncating='post')
X_test_padded = pad_sequences(X_test_sequences, maxlen=MAX_LEN, padding='post', truncating='post')

# Actual vocabulary size including padding token (0)
vocab_size_actual = len(tokenizer.word_index) + 1

print(f"Shape of X_train_padded: {X_train_padded.shape}")
print(f"Shape of X_test_padded: {X_test_padded.shape}")
print(f"Actual vocabulary size: {vocab_size_actual}")

"""#### 2. Model Definition (Self-Attention Model)"""

import tensorflow as tf
from tensorflow.keras.layers import Embedding, GlobalAveragePooling1D, MultiHeadAttention, Dense, Dropout
from tensorflow.keras.models import Model
from tensorflow.keras.optimizers import Adam

EMBEDDING_DIM = 128  # Dimension of the word embeddings
NUM_HEADS = 2        # Number of attention heads
KEY_DIM = EMBEDDING_DIM # Dimension of each attention head's key/query/value

# Input Layer
text_input = tf.keras.Input(shape=(MAX_LEN,), name='text_input')

# Embedding Layer
embedded_sequences = Embedding(
    input_dim=vocab_size_actual,
    output_dim=EMBEDDING_DIM,
    input_length=MAX_LEN,
    name='embedding_layer'
)(text_input)

# MultiHeadAttention Layer (Self-Attention)
# For self-attention, query, key, and value are all the same input sequence.
attention_output = MultiHeadAttention(
    num_heads=NUM_HEADS,
    key_dim=KEY_DIM,
    name='multi_head_attention'
)(embedded_sequences, embedded_sequences)

# Global Average Pooling to reduce sequence dimension for the Dense layer
pooled_output = GlobalAveragePooling1D(name='global_average_pooling')(attention_output)

# Output Layer for classification
classification_output = Dense(
    units=num_classes, # `num_classes` is from Task 3, representing unique medical specialties
    activation='softmax',
    name='output_layer'
)(pooled_output)

# Create the Model
model_attention = Model(inputs=text_input, outputs=classification_output)

# Compile the model
model_attention.compile(
    optimizer=Adam(learning_rate=0.001),
    loss='sparse_categorical_crossentropy',
    metrics=['accuracy']
)

model_attention.summary()

"""#### 3. Model Training"""

history_attention = model_attention.fit(
    X_train_padded, y_train,
    epochs=10,
    batch_size=32,
    validation_split=0.1,
    verbose=1
)

"""#### 4. Model Evaluation"""

from sklearn.metrics import classification_report
import numpy as np

