"""### Task 5: Positional Encoding

Positional encoding is a technique used in transformer models to inject information about the relative or absolute position of the tokens in the sequence. Since the transformer architecture does not inherently process the sequence in order (unlike RNNs), positional encodings are added to the input embeddings to provide this crucial positional information. We'll implement this from scratch and visualize it.
"""

import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

def get_angles(pos, i, d_model):
    angle_rates = 1 / np.power(10000, (2 * (i//2)) / np.float32(d_model))
    return pos * angle_rates

def positional_encoding(position, d_model):
    angle_rads = get_angles(np.arange(position)[:, np.newaxis],
                            np.arange(d_model)[np.newaxis, :],
                            d_model)

    # apply sin to even indices in the array; 2i
    angle_rads[:, 0::2] = np.sin(angle_rads[:, 0::2])

    # apply cos to odd indices in the array; 2i+1
    angle_rads[:, 1::2] = np.cos(angle_rads[:, 1::2])

    pos_encoding = angle_rads[np.newaxis, ...]
    return tf.cast(pos_encoding, dtype=tf.float32)

print(f"Positional Encoding function defined for MAX_LEN={MAX_LEN} and EMBEDDING_DIM={EMBEDDING_DIM}.")

# Generate positional encodings for visualization
# Using MAX_LEN for position and EMBEDDING_DIM for d_model from the self-attention model setup

pe = positional_encoding(MAX_LEN, EMBEDDING_DIM)

# Squeeze the first dimension (batch dimension, which is 1) for plotting
pe_squeezed = pe[0, :, :].numpy()

plt.figure(figsize=(15, 8))
sns.heatmap(pe_squeezed, cmap='viridis')
plt.xlabel('Token Position (Embedding Dimension Index)')
plt.ylabel('Sentence Position')
plt.title('Positional Encoding Visualization (MAX_LEN x EMBEDDING_DIM)')
plt.show()

